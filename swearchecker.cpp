#include "swearchecker.h"

SwearChecker::SwearChecker() {}

QString SwearChecker::swearCheck(const QString& name, const QString& replacement) const
{
    QString filtered = name.toLower();

    QString swear;
    for (const QByteArray& bytes : SwearChecker::swears)
    {
        swear = QString(QByteArray::fromBase64(bytes)).toLower().simplified();
        filtered = filtered.replace(swear, replacement);
    }

    QStringList parts = filtered.split(' ', Qt::SkipEmptyParts);

    for (QString& part : parts)
        part.front() = part.front().toUpper();

    return parts.join(" ");
}

bool SwearChecker::isClean(const QString& name) const
{
    QString check = name.toLower();

    QString swear;
    for (const QByteArray& bytes : SwearChecker::swears)
    {
        swear = QString(QByteArray::fromBase64(bytes)).toLower().simplified();

        if (check.contains(swear)) return false;
    }

    return true;
}

